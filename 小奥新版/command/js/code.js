Code.TABS_ = ['Blocks','Run'];

Code.selected = 'Blocks';

Code.tabClick = function(clickedName) {
  if (clickedName == 'tabXml') {
    var xmlTextarea = document.getElementById('contentXml');
    var xmlText = xmlTextarea.value;
    var xmlDom = null;
    try {
      xmlDom = Blockly.Xml.textToDom(xmlText);
    } catch (e) {
      var q =
          window.confirm(MSG['badXml'].replace('%1', e));
      if (!q) {
        return;
      }
    }
    if (xmlDom) {
      Code.workspace.clear();
      Blockly.Xml.domToWorkspace(xmlDom, Code.workspace);
    }
  }

  if (document.getElementById('tabBlocks').className == 'tabon') {
    Code.workspace.setVisible(false);
  }

  for (var i = 0; i < Code.TABS_.length; i++) {
    var name = Code.TABS_[i];
    document.getElementById('tab' + name).className = 'taboff';
    document.getElementById('content' + name).style.visibility = 'hidden';
  }

  Code.selected = clickedName;

  document.getElementById('contentBlocks').style.visibility = 'visible' ;

  Code.renderContent();
  if (clickedName == 'Blocks') {
    Code.workspace.setVisible(true);
  };

  Blockly.svgResize(Code.workspace);

};

Code.renderContent = function() {
  var content = document.getElementById('content' + Code.selected);
  if (content.id == 'contentXml') {
    var xmlTextarea = document.getElementById('contentXml');
    var xmlDom = Blockly.Xml.workspaceToDom(Code.workspace);
    var xmlText = Blockly.Xml.domToPrettyText(xmlDom);
    xmlTextarea.value = xmlText;
    xmlTextarea.focus();
  } else if (content.id == 'contentRun') {
    var code = Blockly.JavaScript.workspaceToCode(Code.workspace);
    content.textContent = code;
    console.log("code is " + code );
    if (typeof prettyPrintOne == 'function') {
      code = content.innerHTML;
      code = prettyPrintOne(code, 'js');
      content.innerHTML = code;
      console.log("other code is " + code);
    }
  }

};

Code.init = function() {
  console.log("execute init");
  Code.initLanguage();
  var rtl = Code.isRtl();
  var container = document.getElementById('contentArea');
  var onresize = function(e) {
    var bBox = Code.getBBox_(container);
    var el = document.getElementById('contentBlocks');
    el.style.top = bBox.y + 'px';
    el.style.left = bBox.x + 'px';
    el.style.height = bBox.height + 'px';
    el.style.width = bBox.width + 'px';
    var svg = document.getElementsByTagName("svg")[0];
    svg.setAttribute("width",bBox.width + 'px');
  };
  window.addEventListener('resize', onresize, false);

  var toolboxText = document.getElementById('toolbox').outerHTML;
  toolboxText = toolboxText.replace(/{(\w+)}/g,
      function(m, p1) {return MSG[p1]});
  var toolboxXml = Blockly.Xml.textToDom(toolboxText);

  Code.workspace = Blockly.inject('contentBlocks',
      {grid:
      {spacing:20,
        length: 20,
        colour: '#334771',
        snap: true},
        media: 'img/',
        rtl: rtl,
        toolbox: toolboxXml,
        zoom:
        {controls: true,
          wheel: true}
      });

  Blockly.JavaScript.addReservedWords('code,timeouts,checkTimeout');

  Code.loadBlocks('');

  if ('BlocklyStorage' in window) {
    BlocklyStorage.backupOnUnload(Code.workspace);
  }

  Code.tabClick(Code.selected);

  var linkButton = document.getElementById('linkButton');
  if ('BlocklyStorage' in window) {
    BlocklyStorage['HTTPREQUEST_ERROR'] = MSG['httpRequestError'];
    BlocklyStorage['LINK_ALERT'] = MSG['linkAlert'];
    BlocklyStorage['HASH_ERROR'] = MSG['hashError'];
    BlocklyStorage['XML_ERROR'] = MSG['xmlError'];
    Code.bindClick(linkButton,
        function() {BlocklyStorage.link(Code.workspace);});
  } else if (linkButton) {
    linkButton.className = 'disabled';
  }

  for (var i = 0; i < Code.TABS_.length; i++) {
    var name = Code.TABS_[i];
    Code.bindClick('tab' + name,
        function(name_) {return function() {Code.tabClick(name_);};}(name));
  }
  onresize();
  Blockly.svgResize(Code.workspace);

  // Lazy-load the syntax-highlighting.
  window.setTimeout(Code.importPrettify, 1);
};
